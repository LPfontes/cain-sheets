let currentLang = (typeof localStorage !== "undefined" && localStorage.getItem("cain_lang")) || "pt";

export function t(key) {
  return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS.pt[key] || key;
}

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("cain_lang", lang);
  if (document.documentElement) document.documentElement.setAttribute("lang", lang);
  if (document.body) document.body.setAttribute("data-lang", lang);
  applyTranslations();
  window.dispatchEvent(new Event("languageChanged"));
}

export function applyTranslations() {
  if (document.documentElement) document.documentElement.setAttribute("lang", currentLang);
  if (document.body) document.body.setAttribute("data-lang", currentLang);
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const translated = t(key);
    if (translated) {
      if (el.tagName === "INPUT" && el.type !== "file") {
        el.placeholder = translated;
      } else {
        el.innerHTML = translated;
      }
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    const translated = t(key);
    if (translated) {
      el.placeholder = translated;
    }
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    const key = el.getAttribute("data-i18n-title");
    const translated = t(key);
    if (translated) {
      el.title = translated;
    }
  });
}

export function toggleLanguage() {
  setLang(currentLang === "pt" ? "en" : "pt");
}
